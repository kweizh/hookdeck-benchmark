import os
import json
import urllib.request
import pytest

PROJECT_DIR = "/home/user/hookdeck-task"
OUTPUT_FILE = os.path.join(PROJECT_DIR, "request.json")

def test_output_file_exists():
    """Verify that the output JSON file was created."""
    assert os.path.isfile(OUTPUT_FILE), f"Output file {OUTPUT_FILE} does not exist. The script was not executed or failed to save the file."

def test_output_file_is_valid_json():
    """Verify that the output file contains valid JSON."""
    with open(OUTPUT_FILE, "r") as f:
        try:
            json.load(f)
        except json.JSONDecodeError as e:
            pytest.fail(f"Output file {OUTPUT_FILE} is not valid JSON: {e}")

def test_output_matches_target_request_id():
    """Verify that the JSON file contains the correct request ID."""
    target_request_id = os.environ.get("TARGET_REQUEST_ID")
    assert target_request_id, "TARGET_REQUEST_ID environment variable is missing."

    with open(OUTPUT_FILE, "r") as f:
        data = json.load(f)
    
    assert data.get("id") == target_request_id, f"Expected request ID {target_request_id} in output JSON, got {data.get('id')}"

def test_output_matches_api_response():
    """Verify that the JSON file matches the actual API response for the request."""
    target_request_id = os.environ.get("TARGET_REQUEST_ID")
    api_key = os.environ.get("HOOKDECK_API_KEY")
    
    assert target_request_id, "TARGET_REQUEST_ID environment variable is missing."
    assert api_key, "HOOKDECK_API_KEY environment variable is missing."

    # Make direct API call to verify
    req = urllib.request.Request(
        f"https://api.hookdeck.com/2025-07-01/requests/{target_request_id}",
        headers={"Authorization": f"Bearer {api_key}"}
    )
    
    try:
        with urllib.request.urlopen(req) as response:
            api_data = json.loads(response.read().decode("utf-8"))
    except Exception as e:
        pytest.fail(f"Failed to fetch request from Hookdeck API for verification: {e}")

    # Read the file generated by the agent
    with open(OUTPUT_FILE, "r") as f:
        file_data = json.load(f)

    # Verify key fields match
    assert file_data.get("id") == api_data.get("id"), "The 'id' field in the file does not match the API response."
    
    # Check 'data' field since that's the core payload
    file_payload = file_data.get("data", {})
    api_payload = api_data.get("data", {})
    assert file_payload == api_payload, "The 'data' field in the file does not match the API response. Make sure the full response body is saved."
